/**
 * 正则库
 */
const pattern = {
  username: /^([\w-]+|[\u4e00-\u9fa5]+){6,16}$/, // 用户名校验
  passWord: /^\w{6,16}$/, // 密码校验
  phone: /^(?:(?:\+|00)86)?1\d{10}$/, // 手机号校验
  email: /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/, // 邮箱校验
  name: /^(?:[\u4e00-\u9fa5·]{2,16})$/, // 姓名
};

/**
 * 内置规则
 * @param {String} reg - {pattern}中预定义正则名称 or 自定义正则
 * @param {String} msg1 - 正则校验不通过提示
 * @param {String} msg2 - 值为空时提示（可不填）
 */
export const validate = (reg, msg1, msg2) => {
  return (rule, value, callback) => {
    if (!value) {
      if (!msg2) {
        callback();
      }
      callback(new Error(msg2));
    }
    reg = pattern[reg] ? pattern[reg] : reg;
    if (!reg.test(value)) {
      callback(new Error(msg1));
    } else {
      callback();
    }
  };
};

